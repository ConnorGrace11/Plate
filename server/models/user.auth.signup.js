const mongoose = require('mongoose');

const AuthUserSignup = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
})

module.exports = mongoose.model('Auth Users Signup', AuthUserSignup)